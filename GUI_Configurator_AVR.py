    def writeUserConfig(self, filename):
        listPort = ['PORTA', 'PORTB', 'PORTC', 'PORTD']
        name = filename.split('/')[-1].split('.')[0].upper() + '_H'
        
        configFile = open(filename, 'w')
        configFile.write("/*************************************************/\n")
        configFile.write("//            Configuration File\n\n")
        configFile.write("//    File Name: " + filename.split('/')[-1] + "\n")
        configFile.write("//    Date Modified: " + str(date.today().strftime("%B %d, %Y")) + "\n")
        configFile.write("//    Author: " + os.getlogin() + "\n")
        configFile.write("//    Generated by: GUI Configurator made by Ibrahim Saad\n")
        configFile.write("/*************************************************/\n\n\n")
        configFile.write("#ifndef " + name + "\n#define " + name + '\n\n\n')
        configFile.write('#define DIO_OUTPUT_LOW_PIN          0\n#define DIO_OUTPUT_HIGH_PIN         1\n#define DIO_INPUT_HIGH_IMP_PIN      2\n#define DIO_INPUT_PULL_UP_PIN       3\n\n')
        
        for port in range(4):
            configFile.write('\n/* ' + listPort[port] + ' */\n\n')
            for pin in range(8):
                if self.listOfRadios[port][pin][0].isChecked():
                    configFile.write('#define DIO_' + listPort[port] + '_PIN_' + str(pin) + '      DIO_OUTPUT_HIGH_PIN\n')
                if self.listOfRadios[port][pin][1].isChecked():
                    configFile.write('#define DIO_' + listPort[port] + '_PIN_' + str(pin) + '      DIO_OUTPUT_LOW_PIN\n')
                if self.listOfRadios[port][pin][2].isChecked():
                    configFile.write('#define DIO_' + listPort[port] + '_PIN_' + str(pin) + '      DIO_INPUT_HIGH_IMP_PIN\n')
                if self.listOfRadios[port][pin][3].isChecked():
                    configFile.write('#define DIO_' + listPort[port] + '_PIN_' + str(pin) + '      DIO_INPUT_PULL_UP_PIN\n')
        
        configFile.write('\n\n#endif   /* ' + name + ' */')
        configFile.close()
    
