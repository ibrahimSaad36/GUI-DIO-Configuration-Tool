    def prettify(self, element, indent='  '):
        queue = [(0, element)]  # (level, element)
        while queue:
            level, element = queue.pop(0)
            children = [(level + 1, child) for child in list(element)]
            if children:
                element.text = '\n' + indent * (level+1)  # for child open
            if queue:
                element.tail = '\n' + indent * queue[0][0]  # for sibling open
            else:
                element.tail = '\n' + indent * (level-1)  # for parent close
            queue[0:0] = children  # prepend so children come before siblings

    def saveConfigToXml(self, filename):
        ports = ['PORTA', 'PORTB', 'PORTC', 'PORTD']
        root = ET.Element('PORTS')
        for port in range(4):
            portx = ET.SubElement(root, ports[port])
            for pin in range(8):
                portxPinx = ET.SubElement(portx, ports[port][-1] + '_PIN' + str(pin))
                if self.listOfRadios[port][pin][0].isChecked():
                    portxPinx.text = ' OUT_H '
                if self.listOfRadios[port][pin][1].isChecked():
                    portxPinx.text = ' OUT_L '
                if self.listOfRadios[port][pin][2].isChecked():
                    portxPinx.text = ' IN_H_IMP '
                if self.listOfRadios[port][pin][3].isChecked():
                    portxPinx.text = ' IN_P '
        self.prettify(root)
        tree = ET.ElementTree(root)
        #os.chmod(filename, stat.S_IWRITE)
        tree.write(filename, encoding='UTF-8', xml_declaration=True)
